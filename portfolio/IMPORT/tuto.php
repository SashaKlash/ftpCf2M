        <article class="choixTuto">
            <h3>Que choisir ?</h3>
            <p>C'est une question de feeling. Selon moi, il est préférable de travailler sur des tutoriels écrits. Généralement la langue utilisée est très correcte. Certains sont mieux structurés que d'autres. J'apprécie quand le code est présenté étape par étape plutôt qu'un gros bloc commenté ligne par ligne.</p>
            <p>Les tutoriels video peuvent être efficaces. Il faut par contre en essayer plusieurs avant de trouver celui qui vous plaira. D'autant que certains youtubeurs sont plus aguéris, plus pédagogues, plus clairs... que d'autres. J'ai perdu beaucoup de temps pour en trouver trois qui m'ont paru fluides et professionnels.</p>
